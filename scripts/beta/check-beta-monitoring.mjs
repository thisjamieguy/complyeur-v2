import fs from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'

function parseArgs(argv) {
  let baseUrl = process.env.BASE_URL ?? process.env.NEXT_PUBLIC_APP_URL ?? ''
  let dryRun = false
  let outputPath = ''
  let selfTest = false

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index]
    const value = argv[index + 1] ?? ''

    if (arg === '--base-url') {
      baseUrl = value
      index += 1
    } else if (arg === '--output') {
      outputPath = value
      index += 1
    } else if (arg === '--dry-run') {
      dryRun = true
    } else if (arg === '--self-test') {
      selfTest = true
    }
  }

  if (!selfTest && !baseUrl) {
    throw new Error(
      'Missing base URL. Pass --base-url https://example.com or set BASE_URL / NEXT_PUBLIC_APP_URL.'
    )
  }

  return { baseUrl, dryRun, outputPath, selfTest }
}

function normalizeBaseUrl(rawBaseUrl) {
  return rawBaseUrl.replace(/\/+$/, '')
}

function defaultOutputPath(rootDir, dateLabel) {
  return path.join(
    rootDir,
    'docs/operations/evidence',
    `${dateLabel}-beta-monitoring-first-run.md`
  )
}

async function nextAvailablePath(targetPath) {
  const directory = path.dirname(targetPath)
  const extension = path.extname(targetPath)
  const basename = path.basename(targetPath, extension)

  let candidate = targetPath
  let suffix = 2

  while (true) {
    try {
      await fs.access(candidate)
      candidate = path.join(directory, `${basename}-${suffix}${extension}`)
      suffix += 1
    } catch {
      return candidate
    }
  }
}

function buildEvidenceMarkdown(params) {
  const {
    baseUrl,
    cacheControl,
    dateLabel,
    httpStatus,
    result,
  } = params

  const outcomeLine = result.zeroSignupAlert || result.webhookAlert
    ? 'Alert path exercised.'
    : 'No-alert path exercised.'

  return `# Beta Monitoring Cron First-Run Evidence

Date: ${dateLabel}
Environment: ${baseUrl}
Endpoint: ${baseUrl}/api/cron/beta-monitoring
HTTP status: ${httpStatus}
Cache-Control: ${cacheControl}
Result: ${outcomeLine}

## Response Summary

- success: true
- windowHours: ${result.windowHours}
- since: ${result.since}
- companiesCreated: ${result.companiesCreated}
- usersCreated: ${result.usersCreated}
- failedWebhooks: ${result.failedWebhooks}
- staleProcessingWebhooks: ${result.staleProcessingWebhooks}
- zeroSignupAlert: ${result.zeroSignupAlert}
- zeroSignupAlertSent: ${result.zeroSignupAlertSent}
- webhookAlert: ${result.webhookAlert}
- webhookAlertSent: ${result.webhookAlertSent}

## Operator Notes

- This note was generated by \`scripts/beta/check-beta-monitoring.mjs\`.
- No secret values are stored here.
- Attach screenshots or mailbox evidence if alert delivery was expected.
- If both alert flags are false, this note serves as explicit no-alert first-run evidence.
`
}

function validatePayload(payload) {
  if (!payload || typeof payload !== 'object') {
    throw new Error('Monitoring response was not a JSON object.')
  }

  if (payload.success !== true || typeof payload.results !== 'object' || !payload.results) {
    throw new Error('Monitoring response JSON did not contain the expected success/results shape.')
  }

  return payload.results
}

async function main() {
  const options = parseArgs(process.argv.slice(2))
  const rootDir = process.cwd()
  const now = new Date()
  const dateLabel = now.toISOString().slice(0, 10)

  if (options.selfTest) {
    const targetPath = options.outputPath || defaultOutputPath(rootDir, dateLabel)
    console.log(`Monitoring URL: self-test`)
    console.log(`Would write: ${path.relative(rootDir, targetPath)}`)
    console.log('Monitoring probe self-test passed.')
    return
  }

  const cronSecret = process.env.CRON_SECRET
  if (!cronSecret) {
    throw new Error('Missing CRON_SECRET environment variable.')
  }

  const baseUrl = normalizeBaseUrl(options.baseUrl)
  const monitoringUrl = `${baseUrl}/api/cron/beta-monitoring`
  const response = await fetch(monitoringUrl, {
    headers: {
      Accept: 'application/json',
      Authorization: `Bearer ${cronSecret}`,
    },
    cache: 'no-store',
  })

  const cacheControl = response.headers.get('cache-control') ?? 'missing'
  const contentType = response.headers.get('content-type') ?? 'missing'

  if (!contentType.includes('application/json')) {
    const rawBody = await response.text()
    throw new Error(
      `Expected JSON monitoring response, received ${contentType}. Body: ${rawBody.slice(0, 200)}`
    )
  }

  const payload = await response.json()
  const result = validatePayload(payload)

  if (!response.ok) {
    throw new Error(`Monitoring probe failed with http=${response.status}`)
  }

  const requestedOutputPath =
    options.outputPath || defaultOutputPath(rootDir, dateLabel)
  const outputPath = await nextAvailablePath(requestedOutputPath)
  const markdown = buildEvidenceMarkdown({
    baseUrl,
    cacheControl,
    dateLabel,
    httpStatus: response.status,
    result,
  })

  console.log(`Monitoring URL: ${monitoringUrl}`)
  console.log(`HTTP status: ${response.status}`)
  console.log(`Cache-Control: ${cacheControl}`)
  console.log(`zeroSignupAlert=${String(result.zeroSignupAlert)} zeroSignupAlertSent=${String(result.zeroSignupAlertSent)}`)
  console.log(`webhookAlert=${String(result.webhookAlert)} webhookAlertSent=${String(result.webhookAlertSent)}`)

  if (options.dryRun) {
    console.log(`Dry run: would write ${path.relative(rootDir, outputPath)}`)
    return
  }

  await fs.mkdir(path.dirname(outputPath), { recursive: true })
  await fs.writeFile(outputPath, markdown, 'utf8')

  console.log(`Created evidence note: ${path.relative(rootDir, outputPath)}`)
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error)
  console.error(`beta:monitoring:check failed: ${message}`)
  process.exitCode = 1
})
