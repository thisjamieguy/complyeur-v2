import fs from 'node:fs/promises'
import path from 'node:path'
import process from 'node:process'

function parseArgs(argv) {
  const options = {
    deploymentId: '',
    dryRun: false,
    envUrl: '',
    reviewer: '',
    slug: 'beta-verification',
    tester: '',
  }

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index]
    const value = argv[index + 1] ?? ''

    if (arg === '--slug') {
      options.slug = value
      index += 1
    } else if (arg === '--dry-run') {
      options.dryRun = true
    } else if (arg === '--env-url') {
      options.envUrl = value
      index += 1
    } else if (arg === '--deployment-id') {
      options.deploymentId = value
      index += 1
    } else if (arg === '--tester') {
      options.tester = value
      index += 1
    } else if (arg === '--reviewer') {
      options.reviewer = value
      index += 1
    }
  }

  return options
}

function sanitizeSlug(slug) {
  const normalized = slug
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')

  return normalized || 'beta-verification'
}

async function nextAvailablePath(baseDir, baseName) {
  let candidate = path.join(baseDir, `${baseName}.md`)
  let suffix = 2

  while (true) {
    try {
      await fs.access(candidate)
      candidate = path.join(baseDir, `${baseName}-${suffix}.md`)
      suffix += 1
    } catch {
      return candidate
    }
  }
}

async function main() {
  const options = parseArgs(process.argv.slice(2))
  const rootDir = process.cwd()
  const templatePath = path.join(rootDir, 'docs/operations/BETA_EVIDENCE_LOG_TEMPLATE.md')
  const evidenceDir = path.join(rootDir, 'docs/operations/evidence')
  const template = await fs.readFile(templatePath, 'utf8')

  await fs.mkdir(evidenceDir, { recursive: true })

  const now = new Date()
  const dateLabel = now.toISOString().slice(0, 10)
  const baseName = `${dateLabel}-${sanitizeSlug(options.slug)}`
  const outputPath = await nextAvailablePath(evidenceDir, baseName)

  const populated = template
    .replace('- Date:', `- Date: ${dateLabel}`)
    .replace('- Environment URL:', `- Environment URL: ${options.envUrl}`)
    .replace('- Build / deployment ID:', `- Build / deployment ID: ${options.deploymentId}`)
    .replace('- Tester:', `- Tester: ${options.tester}`)
    .replace('- Reviewer:', `- Reviewer: ${options.reviewer}`)

  const banner = [
    '<!-- Generated by scripts/beta/create-evidence-log.mjs -->',
    '',
  ].join('\n')

  if (options.dryRun) {
    console.log(`Dry run: would create ${path.relative(rootDir, outputPath)}`)
    return
  }

  await fs.writeFile(outputPath, `${banner}${populated}`, 'utf8')

  console.log(`Created evidence log: ${path.relative(rootDir, outputPath)}`)
}

main().catch((error) => {
  const message = error instanceof Error ? error.message : String(error)
  console.error(`beta:evidence failed: ${message}`)
  process.exitCode = 1
})
